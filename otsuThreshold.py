import cv2
img=cv2.imread("newspaper.jpg")
gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
cv2.imshow("GrayImage", gray)
cv2.waitKey(0)
gaussian_blur = cv2.GaussianBlur(gray,(11,11),20)
cv2.imshow("GAUSSIAN BLUR",gaussian_blur)
cv2.waitKey(0)
T , otsu_thres = cv2.threshold (gaussian_blur, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
print("The Threshold value = {}".format(T))
cv2.imshow("Threshold", otsu_thres )
cv2.imwrite("simple_ad.jpg", otsu_thres)
cv2.waitKey(0)
